package main

import "fmt"

func main() {

	//Relational Operators --> ==, !=, >, <, >=, <=

	x := 3
	y := 5

	fmt.Printf("x == y : %v\n", x == y)

	fmt.Printf("x != y : %v\n", x != y)

	fmt.Printf("x > y : %v\n", x > y)

	fmt.Printf("x < y : %v\n", x < y)

	fmt.Printf("x >= y : %v\n", x >= y)

	fmt.Printf("x <= y : %v\n", x <= y)
}
